package Projet;
public class Point
{
    private Float x;
    private Float y;
    private Float z;
    
    public Point(final Float x, final Float y, final Float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    
    public Float getX() {
        return this.x;
    }

    public Float getY() {
        return this.y;
    }
    
    public Float getZ() {
        return this.z;
    }
}
